int		ft_atoi(char const *s)
{
	int	n;
	int	sign;

	n = 0;
	sign = 1;
	while (*s == ' ' || (*s >= '\t' && *s <= '\r'))
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	while (*s <= '9' && *s >= '0')
	{
		n = (n * 10) + *s - '0';
		s++;
	}
	return (sign * n);
}
