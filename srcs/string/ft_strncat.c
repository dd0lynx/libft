#include <string.h>

char	*ft_strncat(char *dst, const char *src, size_t n)
{
	char	*p;

	p = dst;
	while (*p != '\0')
		p++;
	while ((*src != '\0') && n--)
	{
		*p = *src;
		p++;
		src++;
	}
	*p = '\0';
	return (dst);
}
